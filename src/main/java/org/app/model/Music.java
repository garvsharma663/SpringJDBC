package org.app.model;

public class Music {

    private String name;
    private String type;

   public Music(){}

    public String getName() {
        return name;
    }

    public String getType() {
        return type;
    }

    public void setName(String name) {
        this.name = name;
    }

    public void setType(String type) {
        this.type = type;
    }
}
